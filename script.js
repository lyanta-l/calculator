const add = function (last, next) {
    last = +last;
    next = +next;
    return last + next;
}

const subtract = function (last, next) {
    last = +last;
    next = +next;
    return last - next;
}

const multiply = function (last, next) {
    last = +last;
    next = +next;
    return last * next;
}

const divide = function (last, next) {
    last = +last;
    next = +next;
    return last / next;
}